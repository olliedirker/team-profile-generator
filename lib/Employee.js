//Employee is the parent class with its components

class Employee {
    constructor(name, id, email) {
      this.name = name;
      this.id = id;
      this.email = email;
    }
  
    getName() {
      return this.name;
    }
  
    getId() {
      return this.id;
    }
  
    getEmail() {
      return this.email;
    }
  
    getTeamTitle() {
      return "Employee";
    }
  }
  
  module.exports = Employee;